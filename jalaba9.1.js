// Шаг 1: Создаём массив с элементами "Джаз" и "Блюз"
let styles = ["Джаз", "Блюз"];

// Шаг 2: Добавляем "Рок-н-ролл" в конец
styles.push("Рок-н-ролл");

// Шаг 3: Заменяем значение в середине на "Классика"
styles[Math.floor(styles.length / 2)] = "Классика";

// Шаг 4: Удаляем первый элемент и выводим его
let removedElement = styles.shift();
console.log(removedElement); // Выводим удалённый элемент

// Шаг 5: Вставляем "Рэп" и "Регги" в начало массива
styles.unshift("Рэп", "Регги");

console.log(styles); // Вывод конечного массива
