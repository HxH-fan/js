function pow(x, n) {
    return x ** n; // Используем оператор возведения в степень
}

// Запрос значений у пользователя
let x = prompt("Введите число x:", "");
let n = prompt("Введите степень n:", "");

if (n < 0) {
    alert("Степень должна быть положительной!");
} else {
    alert(`Результат: ${pow(x, n)}`);
}
